namespace Exercice1
{
    public class Operation
    {

        /// <summary>
        ///  cette methode permet de faire l'addition
        /// </summary>
        /// <param name="a"></param>
        /// <param name="b"></param>
        /// <returns></returns>
        public int addition(int a,int b)
        {
            //retourne la somme de a et b
            return a + b;
        }
        /// <summary>
        /// Cette methode permet de faire la soustraction
        /// </summary>
        /// <param name="a"></param>
        /// <param name="b"></param>
        /// <returns></returns>
        public int soustraction(int a, int b)
        {
            //retourne la difference de a et b
            return a - b;
        }
        /// <summary>
        /// Cette methode permet de faire la multiplication
        /// </summary>
        /// <param name="a"></param>
        /// <param name="b"></param>
        /// <returns></returns>
        public int multiplication(int a, int b)
        {
            //retourne la multiplication de a et b
            return a * b;
        }
        /// <summary>
        /// Cette methode permet de faire la division
        /// </summary>
        /// <param name="a"></param>
        /// <param name="b"></param>
        /// <returns></returns>
        public int division(int a, int b)
        {
            //retourne la division de a et b
            return a / b;
        }
    }
}
